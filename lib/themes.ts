export const SKINS: Record<string, SkinDefinition> = Object.fromEntries(
  SKIN_DEFS.map((s) => [s.id, s]),
);

export function buildSkinCss(skinId: string): string {
  const skin = SKINS[skinId] ?? SKINS.concrete;
  if (!skin?.cssVars) return "";

  const fmt = (obj: Record<string, string>) =>
    Object.entries(obj)
      .map(([k, val]) => `--${k}:${val}`)
      .join(";");

  return `:root{${fmt(skin.cssVars.light)}}.dark{${fmt(skin.cssVars.dark)}}`;
}
